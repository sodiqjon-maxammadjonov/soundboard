  static List<Sound> getSimilarSounds(Sound currentSound, {int limit = 20}) {
    if (currentSound.tags.isEmpty) return [];

    final soundsWithScore = sounds
        .where((s) => s.id != currentSound.id)
        .map((sound) {
          final matchingTags = sound.tags
              .where((tag) => currentSound.tags.contains(tag))
              .length;
          return {'sound': sound, 'score': matchingTags};
        })
        .where((item) => (item['score'] as int) > 0)
        .toList();

    soundsWithScore.sort(
      (a, b) => (b['score'] as int).compareTo(a['score'] as int),
    );

    return soundsWithScore
        .take(limit)
        .map((item) => item['sound'] as Sound)
        .toList();
  }

  static List<Sound> getSoundsByTag(String tag) {
    return sounds.where((sound) => sound.tags.contains(tag)).toList();
  }

  static List<String> getAllTags() {
    final allTags = <String>{};
    for (var sound in sounds) {
      allTags.addAll(sound.tags);
    }
    return allTags.toList()..sort();
  }
